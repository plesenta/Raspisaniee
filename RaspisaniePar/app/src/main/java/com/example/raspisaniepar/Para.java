package com.example.raspisaniepar;

public class Para {

    String den, firstPara, secondPara, thirdPara, fourthPara, fifthPara;

    public Para(String den, String firstPara, String secondPara, String thirdPara, String fourthPara, String fifthPara) {

        this.den = den;
        this.firstPara = firstPara;
        this.secondPara = secondPara;
        this.thirdPara = thirdPara;
        this.fourthPara = fourthPara;
        this.fifthPara = fifthPara;

    }

    public String getDen() {
        return den;
    }

    public String getFirstPara() {
        return firstPara;
    }

    public String getSecondPara() {
        return secondPara;
    }

    public String getThirdPara() {
        return thirdPara;
    }

    public String getFourthPara() {
        return fourthPara;
    }

    public String getFifthPara() {
        return fifthPara;
    }
}
